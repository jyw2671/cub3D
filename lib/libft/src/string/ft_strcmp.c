#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int				n;
	unsigned char	u1;
	unsigned char	u2;

	if ((n = ft_strlen(s1)) != (int)ft_strlen(s2))
		return (1);
	while (n-- > 0)
	{
		u1 = *(unsigned char *)s1++;
		u2 = *(unsigned char *)s2++;
		if (u1 != u2)
			return (u1 - u2);
		if (u1 == '\0')
			return (0);
	}
	return (0);
}
